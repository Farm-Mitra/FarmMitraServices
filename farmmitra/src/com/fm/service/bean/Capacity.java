package com.fm.service.bean;

public class Capacity {
	
	Integer capacity;

	public Capacity() {
		super();
	}

	public Capacity(Integer capacity) {
		super();
		this.capacity = capacity;
	}

	public Integer getCapacity() {
		return capacity;
	}

	public void setCapacity(Integer capacity) {
		this.capacity = capacity;
	}

}
